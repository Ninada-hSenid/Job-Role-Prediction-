import pandas as pd
import numpy as np
tr        = "../Data/train.csv"
ts        = "../Data/test.csv"
train = pd.read_csv(tr)
test  = pd.read_csv(ts)
#wines = np.genfromtxt("./roo1.csv", delimiter=";", skip_header=1)
data = np.loadtxt(tr, delimiter=',', skiprows=1)
print(data.shape)
myu,sig = [],[]
for i in range(13):
    mean = np.mean(data[:,i])
    myu.append(mean)
    sigm = np.sum(np.square(data[:,i]-mean))/(data.shape[0]-1)
    sigm = sigm**0.5
    sig.append(sigm)
    data[:,i] = (data[:,i]-mean)/sigm
                                            
print(myu,sig) 
